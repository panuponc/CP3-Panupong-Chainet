print("----โปรแกรมคำนวณหาค่าอัตราเร็ว----")
s = int(input("กรอกระยะทางที่รถเคลื่อนที่ได้(km) = ")) #ระยะทางที่รถเคลื่อนที่ได้
t = int(input("กรอกเวลาที่รถเคลื่อนที่(hr) = ")) #เวลาที่วัตถุใช้เคลื่อนที่จริง s หรือ hr
v = s/t #อัตราเร็วของการเคลื่อนที่ m/s หรือ km/hr
print("---- อัตราเร็ว = ",v, "km/hr ----")